async function seed() {
  try {
    await sequelize.authenticate();
    
    // Parse the data
    const lines = rawData.split('\n').map(line => line.trim()).filter(line => line.length > 0);
    
    const productosMap = new Map();
    
    for (const line of lines) {
      // Divide by tab or multiple spaces
      const parts = line.split(/\t+/);
      if (parts.length >= 2) {
        const codigo = parts[0].trim();
        const descripcion = parts[1].trim();
        const categoria = descripcion.split(' ')[0] || '';
        
        productosMap.set(codigo, {
          codigo_interno: codigo,
          descripcion: descripcion,
          categoria: categoria
        });
      }
    }

    const productosArr = Array.from(productosMap.values());
    console.log(`Intentando insertar ${productosArr.length} productos únicos...`);

    // Insert ignoring duplicates
    await Producto.bulkCreate(productosArr, {
      ignoreDuplicates: true
    });
    
    console.log('¡Productos insertados exitosamente!');
  } catch (error) {
    console.error('Error insertando productos:', error);
  } finally {
    await sequelize.close();
  }
}

seed();
